<h3>Increase in out-of-pocket expenses</h3><p>
    Ailments are becoming incredibly expensive to cure in India due to double-digit inflation in the healthcare sector. Consequently, this makes a hole in a person's finances.
    Purchasing health insurance can serve as both an emergency savings account and a plan for unexpected medical expenses.</p><br>
<h3>Your group health cover may not be sufficient</h3><p>
    Your entire family may or may not be covered under a group health plan. A group plan's sum insured limit might not be enough to cover all incurred medical costs. Additionally, you and your family will be at risk for unanticipated medical bills whether you are transitioning to a new work or are unemployed.
    Finally, as you age, you might require frequent medical attention. You might not want to put yourself at danger of going without insurance before that.
    A poll found that nearly 95% of Indians are underinsured, with people over 45 being at the greatest risk.</p><br>
